//How to initialize an array
var numbers = [6, 2, 4]

//How to add elements to an array
numbers.append(5)

//Access an array by providing the element number e.g (0-2)
print(numbers[3])

//Count the amount of elements an array
print(numbers.count)

//Removes an element at the 1 index
print(numbers.remove(at: 1))

//Displays the updated array
print(numbers)

let num2 = numbers.sorted()
print(num2)

